#include <stdio.h>
#include <math.h>

#define PI 3.14159265926535

int main(void){
	int i,j=0;
	
	//今回は4×4の行列
	int size=4;
	
	double a[4][4];
	double T1[4][4];
	
	a[0][0]=0;
	a[0][1]=0;
	a[0][2]=2;
	a[0][3]=0;
	
	a[1][0]=0;
	a[1][1]=0;
	a[1][2]=0;
	a[1][3]=0;	//θ１
	
	a[2][0]=0;
	a[2][1]=90;
	a[2][2]=0;
	a[2][3]=0;	//θ２
	
	a[3][0]=2;
	a[3][1]=0;
	a[3][2]=0;
	a[3][3]=0;	//θ３
	
	a[4][0]=2;
	a[4][1]=0;
	a[4][2]=0;
	a[4][3]=0;
	
	//[0][0] = a
	//[0][1] = α
	//[0][2] = d
	//[0][3] = θ
	
	/*0T1*/
	T1[0][0] = cos(a[0][3]*PI/180);
	T1[0][1] = -sin(a[0][3]*PI/180);
	T1[0][2] = 0;
	T1[0][3] = a[0][0];
	
	T1[1][0] = cos(a[0][1]*PI/180)*sin(a[0][3]*PI/180);
	T1[1][1] = cos(a[0][1]*PI/180)*cos(a[0][3]*PI/180);
	T1[1][2] = -sin(a[0][1]*PI/180);
	T1[1][3] = -sin(a[0][1]*PI/180)*a[0][2];
	
	T1[2][0] = sin(a[0][1]*PI/180)*sin(a[0][3]*PI/180);
	T1[2][1] = sin(a[0][1]*PI/180)*cos(a[0][3]*PI/180);
	T1[2][2] = cos(a[0][1]*PI/180);
	T1[2][3] = cos(a[0][1]*PI/180)*a[0][2];
	
	T1[3][0] = 0;
	T1[3][1] = 0;
	T1[3][2] = 0;
	T1[3][3] = 1;
	//変換行列ここまで
	/****************************/
	
	
	/*一行一列成分はa[0][0]、２行２列成分はa[1][1]に入る*/
	
	printf("\n行列0T1は\n");
	for(i=0;i<size;i++){
		for(j=0;j<size;j++){
		printf("%f ",T1[i][j]);
		}
		printf("\n");
	}
	
	//↓チェック用
	printf("出力終わり\n");
	
}
